package entity;

public class Call {
	private int spoon;
	private int chopstick;
	private int tableware;
	private int water;
	private int ice;
	private int tissue;
	private int others;
	
	public int getSpoon() {
		return spoon;
	}
	public void setSpoon(int spoon) {
		this.spoon = spoon;
	}
	public int getChopstick() {
		return chopstick;
	}
	public void setChopstick(int chopstick) {
		this.chopstick = chopstick;
	}
	public int getTableware() {
		return tableware;
	}
	public void setTableware(int tableware) {
		this.tableware = tableware;
	}
	public int getWater() {
		return water;
	}
	public void setWater(int water) {
		this.water = water;
	}
	public int getIce() {
		return ice;
	}
	public void setIce(int ice) {
		this.ice = ice;
	}
	public int getTissue() {
		return tissue;
	}
	public void setTissue(int tissue) {
		this.tissue = tissue;
	}
	public int getOthers() {
		return others;
	}
	public void setOthers(int others) {
		this.others = others;
	}
	
	
}
